 // Returns the current system time in microseconds

//===============================================================================================================================================================================================================200
//	DEFINE/INCLUDE
//===============================================================================================================================================================================================================200

#include <stdlib.h>									// (in path specified to compiler)	needed by OpenCL types and functions

//===============================================================================================================================================================================================================200
//	PROTOTYPES
//===============================================================================================================================================================================================================200

long long 
get_time() {

	struct timeval tv;
	gettimeofday(&tv, NULL);

	return (tv.tv_sec * 1000000) + tv.tv_usec;

}

